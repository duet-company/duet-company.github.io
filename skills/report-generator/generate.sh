#!/bin/bash
# Report Generator Skill - Main Script
# Generates professional reports with Vercel-inspired design

set -e

# Paths
BASE_DIR="$HOME/.openclaw/workspace"
SKILL_DIR="$BASE_DIR/skills/report-generator"
TEMPLATE_DIR="$SKILL_DIR/templates"
DATE=$(date +%Y-%m-%d")
TIMESTAMP=$(date -u +"%Y-%m-%d %H:%M:%S UTC")

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Load CSS template
load_css() {
    if [ -f "$TEMPLATE_DIR/styles/main.css" ]; then
        cat "$TEMPLATE_DIR/styles/main.css"
    else
        # Fallback basic CSS
        cat << 'EOF'
* { margin: 0; padding: 0; box-sizing: border-box; }
body {
    font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
    background: #000000;
    color: #EAEAEA;
    line-height: 1.6;
    padding: 20px;
}
EOF
    fi
}

# Generate HTML report
generate_html() {
    local report_type="$1"
    local data="$2"
    local output_dir="$3"
    
    local output_file="$output_dir/report-$DATE.html"
    local html_file="$output_dir/index.html"
    
    mkdir -p "$output_dir"
    
    echo -e "${BLUE}Generating $report_type HTML...${NC}"
    
    # Build HTML
    local css=$(load_css)
    cat > "$output_file" << EOF
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>$report_type - $DATE</title>
    <meta name="description" content="Professional report generated by OpenClaw">
    <style>
        $css
    </style>
</head>
<body>
    <div class="container">
        <header>
            <div class="header-brand">
                <span class="brand-icon">📊</span>
                <span class="brand-name">$report_type</span>
            </div>
            <h1 class="header-title">Report Generated</h1>
            <div class="header-meta">
                <span class="date">$DATE</span>
                <span class="separator">•</span>
                <span class="time">$TIMESTAMP</span>
            </div>
        </header>
        
        <div class="stats-grid">
            <div class="stat-card">
                <div class="stat-value">Report Generated</div>
                <div class="stat-label">Status</div>
            </div>
            <div class="stat-card">
                <div class="stat-value">Ready</div>
                <div class="stat-label">Action Required</div>
            </div>
        </div>
        
        <div class="section">
            <h2 class="section-title">Report Data</h2>
            <div class="card">
                <div class="card-content">
                    <pre class="code-block"><code>$data</code></pre>
                </div>
            </div>
        </div>
        
        <footer>
            <p>Generated by <a href="https://duet-company.github.io">duyetbot</a> — AI Employee 1 at Duet Company</p>
            <p style="margin-top: 8px;">Report generated using Report Generator Skill</p>
            <p style="margin-top: 8px; color: #5E6AD2;">To publish to here.now, run the publish script in the output directory</p>
        </footer>
    </div>
    
    <script>
        // Simple fade-in animation
        document.addEventListener('DOMContentLoaded', function() {
            document.body.classList.add('fade-in');
        });
    </script>
</body>
</html>
EOF
    
    echo -e "${GREEN}✅ Generated: $output_file${NC}"
    
    # Copy to index.html for here.now
    cp "$output_file" "$html_file"
    
    echo "$output_dir" > "$output_dir/.hereenow-latest"
}

# Publish to here.now
publish_here_now() {
    local output_dir="$1"
    
    echo -e "${BLUE}Publishing to here.now...${NC}"
    
    cd "$output_dir"
    
    # Try to use here.now skill
    if command -v bash &>/dev/null && [ -f "$BASE_DIR/skills/here-now/scripts/publish.sh" ]; then
        bash "$BASE_DIR/skills/here-now/scripts/publish.sh" "index.html" 2>&1 | tail -5
    else
        echo -e "${YELLOW}⚠️ here.now script not found, skipping publish${NC}"
        return 1
    fi
    
    # Get URL from state file
    if [ -f ".herenow/state.json" ]; then
        local url=$(python3 -c "
import json
try:
    with open('.herenow/state.json', 'r') as f:
        data = json.load(f)
        publishes = data.get('publishes', {})
        if publishes:
            first_key = list(publishes.keys())[0]
            url = publishes[first_key].get('siteUrl', 'N/A')
            print(url)
        else:
            print('N/A')
except Exception as e:
    print(f'Error: {e}')
" 2>/dev/null)
        
        if [ "$url" != "N/A" ]; then
            echo -e "${GREEN}✅ Published: $url${NC}"
            echo "$url" > "$output_dir/latest-url.txt"
            echo "$url" > "$output_dir/.hereenow-latest"
        else
            echo -e "${YELLOW}⚠️ Failed to get URL${NC}"
        return 1
    fi
}

# Generate email template
generate_email() {
    local report_type="$1"
    local data="$2"
    local url="$3"
    
    echo -e "${BLUE}Generating email template...${NC}"
    
    cat << 'EOF'
<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <style>
        body {
            font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
            background: #000000;
            color: #EAEAEA;
            line-height: 1.6;
            padding: 20px;
            max-width: 600px;
            margin: 0 auto;
        }
        .header {
            background: #0070F3;
            padding: 30px;
            text-align: center;
            border-radius: 8px;
        }
        .header h1 {
            color: white;
            margin: 0;
            font-size: 1.8rem;
        }
