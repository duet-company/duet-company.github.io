    <div class="cta">
        <a href="$url">View Full Report →</a>
    </div>
    
    <div class="footer">
        <p>Generated at $TIMESTAMP</p>
    </div>
</body>
</html>
EOF
}

# Generate telegram message
generate_telegram() {
    local report_type="$1"
    local data="$2"
    local url="$3"
    
    echo -e "${BLUE}Telegram message preview:${NC}"
    echo ""
    echo "📊 $report_type — $DATE"
    echo ""
    echo "$data"
    echo ""
    echo "🌐 Full Report: $url"
    echo ""
    echo "---"
}

# Main function
main() {
    local action="${1:-help}"
    local report_type="${2:-daily-report}"
    local data="${3:-}"
    local output_dir="${4:-$BASE_DIR/daily-reports}"
    
    case "$action" in
        generate)
            if [ -z "$data" ]; then
                echo -e "${RED}Error: Data parameter required${NC}"
                echo "Usage: $0 generate <report-type> <data> [output-dir]"
                exit 1
            fi
            
            generate_html "$report_type" "$data" "$output_dir"
            ;;
            
        publish)
            publish_here_now "$output_dir"
            ;;
            
        email)
            if [ -z "$data" ] || [ -z "$3" ]; then
                echo -e "${RED}Error: Data and URL parameters required${NC}"
                echo "Usage: $0 email <report-type> <data> <url>"
                exit 1
            fi
            
            generate_email "$report_type" "$data" "$3"
            ;;
            
        telegram)
            if [ -z "$data" ] || [ -z "$3" ]; then
                echo -e "${RED}Error: Data and URL parameters required${NC}"
                echo "Usage: $0 telegram <report-type> <data> <url>"
                exit 1
            fi
            
            generate_telegram "$report_type" "$data" "$3"
            ;;
            
        full)
            generate_html "$report_type" "$data" "$output_dir"
            publish_here_now "$output_dir"
            ;;
            
        help|--help|-h)
            cat << EOF
Report Generator Skill - Vercel Dashboard Design
=============================================

Usage: $0 <action> [options]

Actions:
  generate   Generate HTML report from data
  publish    Publish to here.now
  email      Generate email template
  telegram   Generate Telegram message
  full       Generate, publish, and return URL

Options:
  --report-type <type>     Report type (daily, health, news, etc.)
  --data <data>          Report data (JSON or text)
  --output-dir <dir>       Output directory
  --url <url>              here.now URL

Examples:
  $0 generate --report-type daily --data '{...}' --output-dir ~/reports
  $0 full --report-type health --data 'System healthy'
  $0 telegram --report-type news --data 'AI news items...' --url https://xxx.here.now/
  
Supported Report Types:
  - daily-report: Comprehensive daily overview
  - health-report: System health monitoring
  - ai-news: AI news briefing
  - polymarket-daily: Polymarket daily briefing
  - polymarket-weekly: Polymarket weekly digest
  - task-list: Task progress tracking
  - milestone: Milestone achievements
  - summary: Summary/overview report

Design Features:
  - Vercel dashboard aesthetic (clean, minimal)
  - Unified color palette
  - Responsive design (mobile-first)
  - Email HTML templates (client-safe)
  - Telegram markdown messages
  - Here.now publishing
  - Beautiful data visualization

EOF
            ;;
            
        *)
            echo -e "${RED}Unknown action: $action${NC}"
            echo "Use: $0 help"
            exit 1
            ;;
    esac
}

# Run main
main "$@"
